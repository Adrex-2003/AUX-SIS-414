function suma(x, y){
    return x + y;
}

function resta(x, y){
    return x - y;
}

function multiplicacion(x, y){
    return x * y;
}

function division(x, y){
    return x / y;
}

let x = Number(4);
let y = Number(6);

console.log("**********Operaciones basicas***********")
console.log("suma: " + suma(x, y));
console.log("resta: " + resta(x, y));
console.log("multiplicación: " + multiplicacion(x, y));
console.log("división: " + division(x, y));
